$(document).ready(function () {

    $(function () {
        $.validator.methods.date = function (value, element) {
            if ($.browser.webkit) {

                //ES - Chrome does not use the locale when new Date objects instantiated:
                var d = new Date();

                return this.optional(element) || !/Invalid|NaN/.test(new Date(d.toLocaleDateTimeString(value)));
            }
            else {

                return this.optional(element) || !/Invalid|NaN/.test(new Date(value));
            }
        };
    });

    $('.restricted-menu-items').remove();
});
$(function () {
    $('.check_nameVi').change(function () {
        var inputVi = $('.inputVi').val();
        if ($(this).is(':checked')) {
            $('.inputEng').val(inputVi);
            $(".inputEng").attr('readonly', 'readonly');

        } else {
            $('.inputEng').val("");
            $(".inputEng").removeAttr('readonly', 'readonly');

        }
    });
});